/* ----------------------------------------------------------------------------------- */
int cmpeq_bmatrix(byte **X, long nrl,long nrh,long ncl, long nch, byte **Y)
/* ----------------------------------------------------------------------------------- */
{
	int i, j;
	
	for(i=nrl; i<=nrh; i++) {
		for(j=ncl; j<=nch; j++) {
			if(X[i][j] != Y[i][j]) return 1;
		}
	}
	return 0;
}
