/* --------------------------------------------------------------------- */
void set_si8vector_str(sint8 *v, long nl, long nh, char *str)
/* --------------------------------------------------------------------- */
{
	int i;
	char c[2];
	for(i=nl; i<=nh; i++) {

		*c = *str++;
		if(isdigit(*c))
			v[i] = (sint8) atoi(c);
		else
			v[i] = (sint8) 0;
	}
}
